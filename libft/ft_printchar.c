#include "ft_printf.h"

int	ft_printchar(int c, int *cmp)
{
	if (*cmp == -1)
		return (-1);
	if (write (1, &c, 1) == -1)
	{
		*cmp = -1;
		return (-1);
	}
	return (1);
}
