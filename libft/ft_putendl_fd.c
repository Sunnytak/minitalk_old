#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	len;

	len = 0;
	if (s != '\0')
	{
		while (s[len] != '\0')
			len++;
	}
	write (fd, s, len);
	write (fd, "\n", 1);
}
